package day02;

public class _03_Example2 {
    public static void main(String[] args) {

        System.out.println("Salı");
        System.out.println("************");

        System.out.print("S");
        System.out.print("a");
        System.out.print("l");
        System.out.println("ı");  // When ln, writes ı and moves to the next line

        System.out.println("**************");
        System.out.println("S");
        System.out.println("a");
        System.out.println("l");
        System.out.println("ı");

        System.out.println("**************");

        System.out.println("S\na\nl\nı");  // works like \n = ln
    }

}
